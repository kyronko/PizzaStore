package com.tjedit.pizzastore.datas;

public class PizzaData {
    public String pizzaStoreNameTxt;
    public  String openTimeTxt;
    public  String numberTxt;
    public String imageURL;

    public PizzaData(String pizzaStoreNameTxt, String openTimeTxt, String numberTxt, String imageURL) {
        this.pizzaStoreNameTxt = pizzaStoreNameTxt;
        this.openTimeTxt = openTimeTxt;
        this.numberTxt = numberTxt;
        this.imageURL = imageURL;
    }
}
